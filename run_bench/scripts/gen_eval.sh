#!/bin/bash

# Generate run_#{use_case}.sh with fully expanded benchmark commands (no source at runtime).
# Either: (1) single -B -T -P for one bench+cc and multiple process counts, or
#         (2) --spec-file with lines "BENCH_NAME CC_TYPE NUM_PROCESS" for one script with all runs.

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
source "${SCRIPT_DIR}/../../env.sh"

function usage() {
  echo
  echo "Usage (single bench+cc, multiple -P):"
  echo "  $0 -B <benchmark> -V <version> -C <usecase> -T <cctype> -P <process_list> [-N <nodes>] [-o <output_dir>]"
  echo
  echo "Usage (multiple specs → one script):"
  echo "  $0 -C <usecase> -V <version> --spec-file <file> [-N <nodes>] [-o <output_dir>]"
  echo "  Spec file: one line per run: BENCH_NAME CC_TYPE NUM_PROCESS"
  echo
  echo "  -B, --benchmark     Benchmark name (e.g., osu_pt2pt_mbw, stencil_mpi_ddt_rma_1000)"
  echo "  -V, --version       Net config (e.g., mpi_cxl)"
  echo "  -C, --usecase       Use case string (output: run_<usecase>.sh)"
  echo "  -T, --cctype        CC_TYPE (e.g., nocc, cc_clwb_clflush, ...)"
  echo "  -P, --process       Process list: \"2 4 8 16\" or \"2,4,8,16\""
  echo "  -N, --nodes         Number of nodes (default: 1)"
  echo "  -o, --output_dir    Where to write run_<usecase>.sh (default: current directory)"
  echo "  --spec-file         Path to file with lines: BENCH_NAME CC_TYPE NUM_PROCESS"
  echo "  --append            Append run blocks to existing run_<usecase>.sh (do not rewrite header)"
  echo
}

BENCH_NAME=""
NET_CONFIG=""
USE_CASE=""
CC_TYPE=""
PROCESS_LIST=""
NUM_NODES="1"
OUT_DIR="."
SPEC_FILE=""
APPEND=false

while (( "$#" )); do
  case "$1" in
    -B|--benchmark)
      BENCH_NAME="${2:-}"; shift 2;;
    -V|--version)
      NET_CONFIG="${2:-}"; shift 2;;
    -C|--usecase)
      USE_CASE="${2:-}"; shift 2;;
    -T|--cctype)
      CC_TYPE="${2:-}"; shift 2;;
    -P|--process)
      PROCESS_LIST="${2:-}"; shift 2;;
    -N|--nodes)
      NUM_NODES="${2:-}"; export NUM_NODES; shift 2;;
    -o|--output_dir)
      OUT_DIR="${2:-}"; shift 2;;
    --spec-file)
      SPEC_FILE="${2:-}"; shift 2;;
    --append)
      APPEND=true; shift 1;;
    -H|-h|-?|--help|--usage)
      usage; exit 0;;
    *)
      echo "Error: Invalid option $1" >&2
      usage
      exit 1;;
  esac
done

if [[ -z "${USE_CASE}" || -z "${NET_CONFIG}" ]]; then
  echo "Error: -C usecase and -V version are required." >&2
  usage
  exit 1
fi

USE_SPEC_FILE=false
if [[ -n "${SPEC_FILE}" ]]; then
  USE_SPEC_FILE=true
  if [[ ! -r "${SPEC_FILE}" ]]; then
    echo "Error: Spec file not readable: ${SPEC_FILE}" >&2
    exit 1
  fi
else
  if [[ -z "${BENCH_NAME}" || -z "${CC_TYPE}" || -z "${PROCESS_LIST}" ]]; then
    echo "Error: Without --spec-file, -B, -T, and -P are required." >&2
    usage
    exit 1
  fi
  PROCESS_LIST_NORM="$(echo "${PROCESS_LIST}" | tr ',' ' ' | xargs)"
fi

OUT_FILE="${OUT_DIR%/}/run_${USE_CASE}.sh"
mkdir -p "${OUT_DIR}"

# DIR for generation: run_bench directory (parent of OUT_DIR when OUT_DIR is e.g. run_bench/eval_scripts)
GEN_DIR="$(cd "${OUT_DIR}/.." && pwd)"
export DIR="${GEN_DIR}"

if [[ ! -e "${DIR}/config_settings/${NET_CONFIG}.sh" ]]; then
  echo "ERROR: ${NET_CONFIG}.sh does not exist under ${DIR}/config_settings/" >&2
  exit 1
fi

need_header=true
if [[ "${APPEND}" == true && -e "${OUT_FILE}" ]]; then
  need_header=false
fi

if [[ "${need_header}" == true ]]; then
  # Source config once with dummy NUM_PROCESS/PPN to get CXL_* for the header (config uses them in MPIARGS)
  NUM_PROCESS=1 PPN=1 source "${DIR}/config_settings/${NET_CONFIG}.sh"

  # Write header: no source, only exports and DIR
  {
    echo "#!/bin/bash"
    echo ""
    echo "# Generated by run_bench/scripts/gen_eval.sh (all commands expanded, no source at runtime)"
    echo ""
    echo "export DIR=\"${RUN_DIR}\""
    echo ""
    echo "USE_CASE=\"${USE_CASE}\""
    echo "NET_CONFIG=\"${NET_CONFIG}\""
    echo "NUM_NODES=\"${NUM_NODES}\""
    echo ""

    # Export CXL_* from config (so MPI commands have them at runtime)
    for v in CXL_SHIM_WIN CXL_DAX_RESET CXL_SHIM_VERBOSE CXL_SHIM_ALLOC CXL_SHIM_COPY_SEND CXL_SHIM_COPY_RECV; do
      if [[ -n "${!v+x}" ]]; then
        echo "export ${v}=\"${!v}\""
      fi
    done
    if [[ -n "${CXL_DAX_PATH+x}" && -n "${CXL_DAX_PATH}" ]]; then
      echo "export CXL_DAX_PATH=\"${CXL_DAX_PATH}\""
    else
      echo "export CXL_MEM_SIZE=\"${CXL_MEM_SIZE:-$((4*1024*1024*1024))}\""
    fi
    echo ""

    # cmpi uses MPICH without CXL shim; clear LD_PRELOAD to avoid interference
    if [[ -z "${LD_PRELOAD+x}" || -z "${LD_PRELOAD}" ]]; then
      echo "unset LD_PRELOAD"
      echo "export LD_PRELOAD"
      echo ""
    fi
  } > "${OUT_FILE}"
fi

# Escape a string for safe use inside single quotes in the generated script ( ' -> '\'' )
escape_single_quotes() {
  sed "s/'/'\\\\''/g"
}

# Append one run block: set LOG_DIR, sleep, echo cmd, run cmd.
# Call with: BENCH_NAME, CC_TYPE, NUM_PROCESS already set; PINNING, BENCH_RUN, LD_PRELOAD from sourced env.
write_block() {
  local log_dir_expr="\${DIR}/results/${BENCH_NAME}/${NET_CONFIG}/${CC_TYPE}/${NUM_PROCESS}/${USE_CASE}/\$(date +%Y%m%d%H%M)"
  local full_cmd="${BENCH_RUN} 2>&1 | tee -a \${LOG_DIR}/output.log"
  local escaped_cmd
  escaped_cmd="$(echo "${full_cmd}" | escape_single_quotes)"

  {
    echo "LOG_DIR=\"${log_dir_expr}\""
    echo "mkdir -p \"\${LOG_DIR}\""
    echo "sleep 10"
    echo "echo '${escaped_cmd}' | tee \"\${LOG_DIR}/output.log\""
    echo "${full_cmd}"
    echo ""
  } >> "${OUT_FILE}"
}

if [[ "${USE_SPEC_FILE}" == true ]]; then
  while IFS= read -r line || [[ -n "${line}" ]]; do
    line="$(echo "${line}" | sed 's/^[[:space:]]*//;s/[[:space:]]*$//')"
    [[ -z "${line}" || "${line:0:1}" == "#" ]] && continue
    read -r BENCH_NAME CC_TYPE NUM_PROCESS <<< "${line}"
    export BENCH_NAME CC_TYPE NUM_PROCESS
    export PPN=$((NUM_PROCESS / NUM_NODES))

    if [[ ! -e "${DIR}/cc_type/${CC_TYPE}.sh" ]]; then
      echo "ERROR: ${CC_TYPE}.sh does not exist under ${DIR}/cc_type/" >&2
      exit 1
    fi
    if [[ ! -e "${DIR}/bench_cmds/${BENCH_NAME}/prepare.sh" ]]; then
      echo "ERROR: ${BENCH_NAME}/prepare.sh does not exist under ${DIR}/bench_cmds/" >&2
      exit 1
    fi
    source "${DIR}/cc_type/${CC_TYPE}.sh"
    source "${DIR}/config_settings/${NET_CONFIG}.sh"
    source "${DIR}/bench_cmds/${BENCH_NAME}/prepare.sh"
    write_block
  done < "${SPEC_FILE}"
else
  for NUM_PROCESS in ${PROCESS_LIST_NORM}; do
    export NUM_PROCESS
    export PPN=$((NUM_PROCESS / NUM_NODES))

    if [[ ! -e "${DIR}/cc_type/${CC_TYPE}.sh" ]]; then
      echo "ERROR: ${CC_TYPE}.sh does not exist under ${DIR}/cc_type/" >&2
      exit 1
    fi
    if [[ ! -e "${DIR}/bench_cmds/${BENCH_NAME}/prepare.sh" ]]; then
      echo "ERROR: ${BENCH_NAME}/prepare.sh does not exist under ${DIR}/bench_cmds/" >&2
      exit 1
    fi
    source "${DIR}/cc_type/${CC_TYPE}.sh"
    source "${DIR}/config_settings/${NET_CONFIG}.sh"
    source "${DIR}/bench_cmds/${BENCH_NAME}/prepare.sh"
    write_block
  done
fi

chmod +x "${OUT_FILE}"
echo "Wrote ${OUT_FILE}"
